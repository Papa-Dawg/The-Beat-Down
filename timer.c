volatile uint32_t ms_counter = 0;             // global ms counter
//======================================================================================================================
//                                                   Functions
//======================================================================================================================
void timer_init (void)
{
	/*-------------------------- Timer 0 Settings ----------------------------*/
	TCCR0A |= (1<<WGM01);                     // CTC mode
	OCR0A   = 249;                            // 1ms at 16MHz, prescaler 64
	TCCR0B |= (1<<CS01)|(1<<CS00);            // prescaler 64
	TIMSK0 |= (1<<OCIE0A);                    // enable compare interrupt
	
	/*-------------------------- Timer 1 Settings ----------------------------*/
	//TCCR1B |= (1<<WGM12);                     // enables CTC mode.
	//TIMSK1 |= (1<<OCIE1A);                    // enables Timer1 compare interrupt.

	/*-------------------------- Timer 2 Settings ----------------------------*/
	TCCR2A |= (1<<WGM21);                     // enables CTC mode.
	OCR2A   = 249;                            // !!! FIX: Changed 'TIMER' to a numeric value or macro
}

//======================================================================================================================
//                                         Interrupt Service Routines
//======================================================================================================================
ISR (TIMER0_COMPA_vect)
{
	ms_counter++;                             // increments every 1ms
}

